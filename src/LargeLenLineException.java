public class LargeLenLineException extends RuntimeException{
    public LargeLenLineException(){
        super();
    }
    public LargeLenLineException(String message){
        super(message);
    }
    public LargeLenLineException(String message, Throwable cause){
        super(message, cause);
    }
    public LargeLenLineException(Throwable cause){
        super(cause);
    }
}
